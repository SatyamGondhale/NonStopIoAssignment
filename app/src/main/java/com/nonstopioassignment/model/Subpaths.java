package com.nonstopioassignment.model;

public class Subpaths {
    private String subPathId;
    private String subPathTitle;
    private String subPathImageUrl;

    public String getSubPathId() {
        return subPathId;
    }

    public void setSubPathId(String subPathId) {
        this.subPathId = subPathId;
    }

    public String getSubPathTitle() {
        return subPathTitle;
    }

    public void setSubPathTitle(String subPathTitle) {
        this.subPathTitle = subPathTitle;
    }

    public String getSubPathImageUrl() {
        return subPathImageUrl;
    }

    public void setSubPathImageUrl(String subPathImageUrl) {
        this.subPathImageUrl = subPathImageUrl;
    }
}
